#Comparacion igualitario, verifica si ambos valores son iguales
igual_que = 5 == 6
print(igual_que)

#Comparacion distintivo, verifica si ambos valores son diferentes
diferente_que = 5 != 6
print(diferente_que)

#Mayor que
mayor_que = 5 > 6
print(mayor_que)

#Menor que
menor_que = 5 < 6
print(menor_que)

#Mayor o igual
mayor_o_igual = 5 >= 6
print(mayor_o_igual)

#Menor o igual
menor_o_igual = 5 <= 6
print(menor_o_igual)

#Calculos Combinados
num_1 = 5
num_2 = 10
num_3 = 20

comparacion = (num_1 + num_2) == num_3
print(comparacion)

